#include "libft.h"

void	ft_lstadd_front(t_list	**alst, t_list	*new)
{
	if (alst)
	{
		if (*alst)
		{
			new -> next = *alst;
			(*alst)->prev = new;
		}
		*alst = new;
	}
}
